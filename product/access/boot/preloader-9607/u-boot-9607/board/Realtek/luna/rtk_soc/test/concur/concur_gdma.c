unsigned int gdma_memset_pat;

/* Memory case 0: Memory set */
unsigned int GDMA_Memory_case0_setting(unsigned int gdma_base, unsigned int burst\
					, unsigned int src_addr, unsigned int src_size\
					, unsigned int dist_addr, unsigned int dist_size)
{
        unsigned int data_size;
        unsigned int data_addr_d;
	unsigned int i, mod_offset;
        volatile unsigned char *data_addr_s;
	unsigned int *memset_dbuf, *memset_sbuf;

	gdma_memset_pat = 0x11223344;

	memset_sbuf = (unsigned int *)src_addr;
	memset_dbuf = (unsigned int *)dist_addr;
	
	//printf("gdma(0x%08x), burst(%d), src_addr(0x%08x), src_size(0x%08x), dist_addr(0x%08x), dist_size(0x%08x)\n",\
			 gdma_base, burst, src_addr, src_size, dist_addr, dist_size);

	/* Clear source & distination buffer */
	for(i=0; i< src_size/sizeof(unsigned int);i++){
		memset_sbuf[i] = 0;
	}
	for(i=0; i< dist_size/sizeof(unsigned int);i++){
		memset_dbuf[i] = 0;
	}

        data_size = dist_size/8;
        data_addr_d = (unsigned int)memset_dbuf;

        /* Reset GDMA */
        REG32(gdma_base+0x00) = 0 ;
        REG32(gdma_base+0x00) = GDMA_ENABLE;
        REG32(gdma_base+0x04) = 0;
        REG32(gdma_base+0x08) = 0xffffffff;
        REG32(gdma_base+0x0c) = gdma_memset_pat;
        REG32(gdma_base+0x10) = 0;
        REG32(gdma_base+0x60) = TO_PHY_ADDR(data_addr_d);
        REG32(gdma_base+0x64) = data_size-1;

	data_addr_s = (unsigned char *)memset_sbuf;
	for(i=0; i < (data_size-1); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<((i%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (data_size-1)%4;

        REG32(gdma_base+0x68) = TO_PHY_ADDR(data_addr_d + (1*data_size));
        REG32(gdma_base+0x6C) = data_size-2;

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s += data_size;
	for(i=0; i < (data_size-2); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (mod_offset + (data_size-2)%4)%4;

        REG32(gdma_base+0x70) = TO_PHY_ADDR(data_addr_d + (2*data_size));
        REG32(gdma_base+0x74) = data_size-3;

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s = data_addr_s + 2*data_size;
	for(i=0; i < (data_size-3); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (mod_offset + (data_size-3)%4)%4;

        REG32(gdma_base+0x78) = TO_PHY_ADDR(data_addr_d + (3*data_size));
        REG32(gdma_base+0x7C) = data_size-4;

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s = data_addr_s + 3*data_size;
	for(i=0; i < (data_size-4); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (mod_offset + (data_size-4)%4)%4;


        REG32(gdma_base+0x80) = TO_PHY_ADDR(data_addr_d + (4*data_size));
        REG32(gdma_base+0x84) = data_size-5;

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s = data_addr_s + 4*data_size;
	for(i=0; i < (data_size-5); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (mod_offset + (data_size-5)%4)%4;

        REG32(gdma_base+0x88) = TO_PHY_ADDR(data_addr_d + (5*data_size));
        REG32(gdma_base+0x8C) = data_size-6;

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s = data_addr_s + 5*data_size;
	for(i=0; i < (data_size-6); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (mod_offset + (data_size-6)%4)%4;

        REG32(gdma_base+0x90) = TO_PHY_ADDR(data_addr_d + (6*data_size));
        REG32(gdma_base+0x94) = data_size-7;

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s = data_addr_s + 6*data_size;
	for(i=0; i < (data_size-7); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}
	mod_offset = (mod_offset + (data_size-7)%4)%4;

        REG32(gdma_base+0x98) = TO_PHY_ADDR(data_addr_d + (7*data_size));
        REG32(gdma_base+0x9C) = (GDMA_LDB|(data_size-8));

	data_addr_s = (unsigned char *)memset_sbuf;
	data_addr_s = data_addr_s + 7*data_size;
	for(i=0; i < (data_size-8); i++){
		*data_addr_s = (unsigned char)((gdma_memset_pat<<(((i+mod_offset)%4)*8))>>24);
		data_addr_s++;
	}

        _cache_flush();

	switch (burst){
		case 4:
        		return (GDMA_ENABLE|GDMA_POLL|GDMA_MEMSET);
			break;
		case 8:
        		return (GDMA_ENABLE|GDMA_POLL|GDMA_MEMSET|0x40);
			break;
		case 16:
	        	return (GDMA_ENABLE|GDMA_POLL|GDMA_MEMSET|0x80);
			break;
		default:
        		return (GDMA_ENABLE|GDMA_POLL|GDMA_MEMSET|0xC0);
			break;
	}
	return 0;
}

