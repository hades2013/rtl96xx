/* MIC case 0 */
unsigned int GDMA_MIC_case0_setting(unsigned int gdma_base, unsigned int burst\
                                        , unsigned int src_addr, unsigned int src_size\
                                        , unsigned int dist_addr, unsigned int dist_size)
{
        unsigned int data_size;
        unsigned int data_addr;
	volatile unsigned int *buff_addr;
	unsigned int i;

        data_size = sizeof(gdma_data);
	buff_addr = (volatile unsigned int *)src_addr;
	i = 0;

	/* Setting data */
	while(data_size){
        	buff_addr[i] = (unsigned int)gdma_data[i];
		i++;
		data_size = data_size - 4;
	}

        _cache_flush();
#if 0
	/* Verify data */
        data_size = sizeof(gdma_data);
	buff_addr = (volatile unsigned int *)src_addr;
	i = 0;
	while(data_size){
        	if(buff_addr[i] != (unsigned int)gdma_data[i]){
			printf("%s, %d: data init fail, target(0x%08x):0x%08x != src(0x%08x):0x%08x08\n",\
				__FUNCTION__, __LINE__, &buff_addr[i], buff_addr[i], &gdma_data[i], gdma_data[i]);
		}
		i++;
		data_size = data_size - 4;
	}
#endif
        data_size = (sizeof(gdma_data)/8);
        data_addr = src_addr;
        //data_addr = (unsigned int)gdma_data;

        /* Reset GDMA */
        REG32(gdma_base+0x00) = 0 ;
        REG32(gdma_base+0x00) = GDMA_ENABLE;
        REG32(gdma_base+0x04) = 0;
        REG32(gdma_base+0x08) = 0xffffffff;
        REG32(gdma_base+0x0C) = 0;
        REG32(gdma_base+0x10) = 0;
        REG32(gdma_base+0x20) = TO_PHY_ADDR(data_addr);
        REG32(gdma_base+0x24) = data_size;
        REG32(gdma_base+0x28) = TO_PHY_ADDR(data_addr+(1*data_size));
        REG32(gdma_base+0x2C) = data_size;
        REG32(gdma_base+0x30) = TO_PHY_ADDR(data_addr+(2*data_size));
        REG32(gdma_base+0x34) = data_size;
        REG32(gdma_base+0x38) = TO_PHY_ADDR(data_addr+(3*data_size));
        REG32(gdma_base+0x3C) = data_size;
        REG32(gdma_base+0x40) = TO_PHY_ADDR(data_addr+(4*data_size));
        REG32(gdma_base+0x44) = data_size;
        REG32(gdma_base+0x48) = TO_PHY_ADDR(data_addr+(5*data_size));
        REG32(gdma_base+0x4C) = data_size;
        REG32(gdma_base+0x50) = TO_PHY_ADDR(data_addr+(6*data_size));
        REG32(gdma_base+0x54) = data_size;
        REG32(gdma_base+0x58) = TO_PHY_ADDR(data_addr+(7*data_size));
        REG32(gdma_base+0x5C) = (GDMA_LDB|data_size);

	switch (burst){
		case 4:
        		return (GDMA_ENABLE|GDMA_POLL|GDMA_MIC);
			break;
		case 8:
        		return (GDMA_ENABLE|GDMA_POLL|GDMA_MIC|0x40);
			break;
		case 16:
	        	return (GDMA_ENABLE|GDMA_POLL|GDMA_MIC|0x80);
			break;
		default:
        		return (GDMA_ENABLE|GDMA_POLL|GDMA_MIC|0xC0);
			break;
	}
	return 0;
}

/* Check result data for MIC case 0 */
int GDMA_MIC_case0_checking( unsigned int gdma_base, unsigned int src_addr, \
                          unsigned int src_size, unsigned int dist_addr, unsigned int dist_size)
{
        if(REG32(gdma_base+0x0C)!= (0xa769eb37) || REG32(gdma_base+0x10) != (0xb1da5a0e)){
                printf("error ! ICVL = 0x%08x, ICVR = 0x%08x\n", REG32(gdma_base+0x0C), REG32(gdma_base+0x10));
                return CHECKING_FAIL;
        }
        return CHECKING_PASS;
}

