GOS_ERROR_CODE Map8021pServProfConnCfg(void* pOldRow,void* pNewRow,MIB_OPERA_TYPE  operationType)
{

	MIB_TABLE_MAP8021PSERVPROF_T*  map8021;
	MIB_TREE_T *pTree;
	MIB_ENTRY_T *pEntry;
	int key;
	
	switch (operationType){
	case MIB_ADD:
	{
		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"802.1p Mapper Configure ---- > ADD");

		/*check if new connection is complete*/
		map8021 = (MIB_TABLE_MAP8021PSERVPROF_T *)pNewRow;
		/*find avl tree*/
		pTree =Map8021pServProfGetTree(map8021);
		if(pTree==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Can't find Root Tree");
			return GOS_OK;
		}
		/*add new node to tree*/
		key = AVL_KEY_MAP8021PSERVPROF;

		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"key is %d",key);
		if(MIB_AvlTreeNodeAdd(&pTree->root,key,MIB_TABLE_MAP8021PSERVPROF_INDEX,map8021)==NULL)
		{				
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Add 8021p Mapper Node Fail");
			return GOS_OK;
		}
	}
	break;
	case MIB_SET:
	{			
		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"802.1p Mapper Configure ---- > SET");
		/*check if new connection is complete*/
		map8021 = (MIB_TABLE_MAP8021PSERVPROF_T *)pNewRow;
		/*find avl tree*/
		pTree =Map8021pServProfGetTree(map8021);
		if(pTree==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Can't find Root Tree");
			return GOS_OK;
		}
      	
	}
	break;
	case MIB_DEL:
	{			
		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"802.1p Mapper Configure ---- > DEL");
		 /*check if new connection is complete*/
		map8021 = (MIB_TABLE_MAP8021PSERVPROF_T *)pOldRow;
		/*find avl tree*/
		pTree =Map8021pServProfGetTree(map8021);
		if(pTree==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Can't find Root Tree");
			return GOS_FAIL;
		}
		/*add new node to tree*/
		key = AVL_KEY_MAP8021PSERVPROF;

		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"key is %d",key);
		pEntry = MIB_AvlTreeEntrySearch(pTree->root,key,map8021->EntityID);

		if(pEntry==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Search Port entry faild");
			return GOS_OK;
		}
		/*remove node from tree*/
		LIST_REMOVE(pEntry,treeEntry);

	}
	break;
	default:
	break;
	}
	/*check connection*/
	if(pTree)
		MIB_TreeConnUpdate(pTree);

	
	return GOS_OK;
}

