GOS_ERROR_CODE MacBriPortCfgDataConnCfg(void* pOldRow,void* pNewRow,MIB_OPERA_TYPE  operationType)
{
	MIB_TABLE_MACBRIPORTCFGDATA_T* pPort;
	MIB_TREE_T *pTree;
	MIB_ENTRY_T	*pEntry;
	int key;

	switch (operationType){
	case MIB_ADD:
	{
    		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Bridge Port Configure ---- > ADD"); 
		/*check if new connection is complete*/
		pPort = (MIB_TABLE_MACBRIPORTCFGDATA_T *)pNewRow;
		key = pPort->TPType == TP_TYPE_PPTP_ETHUNI ? AVL_KEY_MACBRIPORT_UNI : AVL_KEY_MACBRIPORT_ANI;
		/*find avl tree*/
		pTree = MIB_AvlTreeSearchByKey(pPort->BridgeIdPtr,AVL_KEY_MACBRISERVPROF);
		if(pTree==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Can't find Root Tree");
			return GOS_OK;
		}
		/*add new node to tree*/
		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"key is %d, TP Type %d, TP Ptr %x",key,pPort->TPType,pPort->TPPointer);
		if(MIB_AvlTreeNodeAdd(&pTree->root,key,MIB_TABLE_MACBRIPORTCFGDATA_INDEX,pPort)==NULL)
		{
			return GOS_OK;
		}
		if(key == AVL_KEY_MACBRIPORT_UNI)
		{
			EthUniAvlTreeAdd(pTree,pPort->TPPointer);
		}
		if(pPort->TPType == TP_TYPE_8021P)
		{
			Map8021pAvlTreeAdd(pTree,pPort->TPPointer);	
		}
		if(pPort->TPType == TP_TYPE_MULTIGEMIWTP)
		{
			McastGemIwTpAvlTreeAdd(pTree,pPort->TPPointer);
		}
	break;
	}
	case MIB_SET:
	{
    	OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Bridge Port Configure ---- > SET");
		/*check if new connection is complete*/
    	pPort = (MIB_TABLE_MACBRIPORTCFGDATA_T *)pNewRow;
		key = pPort->TPType == TP_TYPE_PPTP_ETHUNI ? AVL_KEY_MACBRIPORT_UNI : AVL_KEY_MACBRIPORT_ANI;
    	/*find avl tree*/
		pTree = MIB_AvlTreeSearchByKey(pPort->BridgeIdPtr,AVL_KEY_MACBRISERVPROF);
		if(pTree==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Can't find Root Tree");
			return GOS_OK;
		}
		/*add new node to tree*/
		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"key is %d, TP Type %d, TP Ptr %x",key,pPort->TPType,pPort->TPPointer);
		if(MIB_AvlTreeNodeAdd(&pTree->root,key,MIB_TABLE_MACBRIPORTCFGDATA_INDEX,pPort)==NULL)
		{
			return GOS_OK;
		}
		if(key == AVL_KEY_MACBRIPORT_UNI)
		{
			EthUniAvlTreeAdd(pTree,pPort->TPPointer);
		}else
		if(key == AVL_KEY_MAP8021PSERVPROF)
		{	
			Map8021pAvlTreeAdd(pTree,pPort->TPPointer);	
		}
	break;
	}
	case MIB_DEL:
	{
    	OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Bridge Port Configure ---- > DEL");
		/*check if new connection is complete*/
        pPort = (MIB_TABLE_MACBRIPORTCFGDATA_T *)pOldRow;
		key = pPort->TPType == TP_TYPE_PPTP_ETHUNI ? AVL_KEY_MACBRIPORT_UNI : AVL_KEY_MACBRIPORT_ANI;
            	/*find avl tree*/
		pTree = MIB_AvlTreeSearchByKey(pPort->BridgeIdPtr,AVL_KEY_MACBRISERVPROF);
		if(pTree==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Can't find Root Tree");
			return GOS_OK;
		}
		pEntry = MIB_AvlTreeEntrySearch(pTree->root,key,pPort->EntityID);

		if(pEntry==NULL)
		{
			OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Search Port entry faild");
			return GOS_OK;
		}
		/*remove node from tree*/
		LIST_REMOVE(pEntry,treeEntry);
	break;
	}
	default:
	    return GOS_OK;
	}

	if(pTree)
	{
		MIB_TreeConnUpdate(pTree);
	}
	return GOS_OK;
}

