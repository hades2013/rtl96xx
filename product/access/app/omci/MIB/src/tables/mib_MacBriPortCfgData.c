GOS_ERROR_CODE MacBriPortCfgDataConnCheck(MIB_TREE_T *pTree,MIB_TREE_CONN_T *pConn,PON_ME_ENTITY_ID entityId,
int parm)
{
	int index=0;
	GOS_ERROR_CODE ret;
	MIB_ENTRY_T *pEntry;
	MIB_TABLE_MACBRIPORTCFGDATA_T *pData;
	MIB_AVL_KEY_T key = (MIB_AVL_KEY_T)parm;

	OMCI_LOG(OMCI_LOG_LEVEL_LOW,"Start %s...",__FUNCTION__);
	/*check uni*/
	pEntry = MIB_AvlTreeEntrySearch(pTree->root,key,entityId);
	/*if not found, check ani*/
	if(!pEntry){
		OMCI_LOG(OMCI_LOG_LEVEL_LOW,"%s: can't find MacBridgePortCfgData",__FUNCTION__);
		return GOS_FAIL;
	}
	
	pData = (MIB_TABLE_MACBRIPORTCFGDATA_T*)pEntry->pData;

	OMCI_LOG(OMCI_LOG_LEVEL_LOW,"%s:  MacBridgePortCfGData TP Type is %x",__FUNCTION__,pData->TPType);

	switch(pData->TPType)
	{
	case TP_TYPE_8021P:
	{		
		pConn->traffMode= OMCI_TRAF_MODE_8021P_BASE;
		pConn->pAniPort= pData;
		ret = OMCI_MeOperConnCheck(MIB_TABLE_MAP8021PSERVPROF_INDEX,pTree,pConn,pData->TPPointer,index);		
	}
	break;
	case TP_TYPE_GEMIWTP:
	{		
		pConn->traffMode= OMCI_TRAF_MODE_FLOW_BASE;
		pConn->pAniPort = pData;
		ret = OMCI_MeOperConnCheck(MIB_TABLE_GEMIWTP_INDEX,pTree,pConn,pData->TPPointer,index);		
	}
	break;
	case TP_TYPE_MULTIGEMIWTP:		
	{		
		pConn->traffMode= OMCI_TRAF_MODE_FLOW_BASE;
		pConn->pAniPort = pData;
		ret = OMCI_MeOperConnCheck(MIB_TABLE_MULTIGEMIWTP_INDEX,pTree,pConn,pData->TPPointer,index);		
	}
	break;
	case TP_TYPE_PPTP_ETHUNI:
	{
		
		pConn->pUniPort = pData;
		ret = OMCI_MeOperConnCheck(MIB_TABLE_ETHUNI_INDEX,pTree,pConn,pData->TPPointer,index);
	}
	break;
	default:
		ret = GOS_FAIL;
	break;
	}

	/*update vlan action related ME*/
	if(ret == GOS_OK)
	{
		OMCI_MeOperConnCheck(MIB_TABLE_VLANTAGFILTERDATA_INDEX,pTree,pConn,pData->EntityID,index);
		OMCI_MeOperConnCheck(MIB_TABLE_VLANTAGOPCFGDATA_INDEX,pTree,pConn,pData->EntityID,index);
	}

	return ret;
}


